    public void populateTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data
        try {
            Connection conn = backend.ConnectionManager.getConnection();
            String query = "SELECT * FROM Vehicule";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id_vehicule"),
                    rs.getString("marque"),
                    rs.getString("modele"),
                    rs.getInt("annee"),
                    rs.getString("type"),
                    rs.getString("carburant"),
                    rs.getInt("prix_location_jour"),
                    rs.getString("etat")
                };
                model.addRow(row);
            }
            System.out.println("Table populated successfully!");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    public void populateTableFromArrayList(ArrayList<Vehicule> vehicules) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data

        for (Vehicule vehicule : vehicules) {
            Object[] row = {
            		vehicule.getIdVehicle(),
            		vehicule.getMarque(),
            		vehicule.getModele(),
            		vehicule.getAnnee(),
            		vehicule.getType(),
            		vehicule.getCarburant(),
            		vehicule.getPrixLocationJour(),
            		vehicule.getEtat()
            };
            model.addRow(row);
        }
        System.out.println("Table populated successfully!");
    }
    
