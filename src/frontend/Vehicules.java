    private void supprimerBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerBTNActionPerformed
        // Get the selected row index
        int selectedRow = jTable1.getSelectedRow();

        // Check if a row is selected
        if (selectedRow != -1) {
            // Get the value in the first column (index 0) of the selected row
            Object value = jTable1.getValueAt(selectedRow, 0);

            // Display a confirmation dialog
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Are you sure you want to delete this vehicule?",
                    "Confirm Deletion",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE
            );

            // If user confirms deletion
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    int id = Integer.parseInt(value.toString());
                    Vehicule.deleteVehicule(conn, id);
                    populateTable(); // Refresh the table
                    JOptionPane.showMessageDialog(this, "Vehicule deleted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error deleting vehicule: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            // Warn the user to select a row
            JOptionPane.showMessageDialog(
                    this,
                    "Please select a user to delete.",
                    "No Selection",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_supprimerBTNActionPerformed

