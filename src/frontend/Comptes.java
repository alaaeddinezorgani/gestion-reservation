    public void populateTable() throws SQLException {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data
        try {
            Connection conn = DriverManager.getConnection("jdbc:sqlite:/home/alaaeddine/ws/eclipse/Eag/src/backend/gestion-reservation.db");
            String query = "SELECT * FROM Utilisateur";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id_utilisateur"),
                    rs.getString("nom"),
                    rs.getString("prenom"),
                    rs.getString("email"),
                    rs.getString("mot_de_passe"),
                    rs.getString("role")
                };
                model.addRow(row);
            }
            //conn.close();
            System.out.println("Table populated successfully!");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    public void populateTableFromArrayList(ArrayList<Gestionnaire> gestionnaires) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data

        for (Gestionnaire gestionnaire : gestionnaires) {
            Object[] row = {
            	gestionnaire.getIdUtilisateur(),
            	gestionnaire.getNom(),
            	gestionnaire.getPrenom(),
            	gestionnaire.getEmail(),
            	gestionnaire.getMotDePasse(),
            	gestionnaire.getRole()
            };
            model.addRow(row);
        }
        System.out.println("Table populated successfully!");
    }


